
//switch case break and default=-------------

num = 10
switch(num){
    case 1000:
        console.log('I am an 1000');
    break;
    case 100:
        console.log('I am an 100')
    break;
    case 20:
    case 10:
        console.log('I am either 10 or 20')
        break;
    default:
        console.log("I don't know who you are")

}


